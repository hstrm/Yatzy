    private void btnYatzyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnYatzyActionPerformed
        //ta in värdet på första platsen i listan
        int tarning1 = tarningLista[0];
        //räkna antal samma sidor
        int sammaSida = 0;
        for (int i = 0; i < tarningLista.length; i++) {
            //Om någon annan plats i listan har ett annat värde än tarning1 så avbryt
            if (tarning1 != tarningLista[i]){
                break;
            }
            //Om något har samma värde ge 50 poäng
            else{
                sammaSida++;
            }
        }
        //om antal sammaSida är lika med längden på tarningLista (5) så ge 50 poäng
        if (sammaSida == tarningLista.length){
            txfSumma.setText(String.valueOf(totalSumma += 50));
            txfYatzy.setText("50");
        }
        //annars ge 0 poäng
        else {
            txfYatzy.setText("0");
        }
        
        btnYatzy.setEnabled(false);
        
        nyRunda();
    }//GEN-LAST:event_btnYatzyActionPerformed

