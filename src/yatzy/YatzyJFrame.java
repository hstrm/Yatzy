    private void btnKastaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKastaActionPerformed
        //anropa metoden för att kasta tärningar som inte är låsta
        if(antalKast > 0){
            kastaTarning();
        
            //räkna ner antal kast
            antalKast--;
            lblKast.setText("Kast kvar: " + String.valueOf(antalKast));
        }
        
        
        //spara alla resultat i en lista
        tarningLista[0] = Integer.valueOf(btnDice1.getText());
        tarningLista[1] = Integer.valueOf(btnDice2.getText());
        tarningLista[2] = Integer.valueOf(btnDice3.getText());
        tarningLista[3] = Integer.valueOf(btnDice4.getText());
        tarningLista[4] = Integer.valueOf(btnDice5.getText());
        
        //nollställ listan
        for (int i = 0; i < tarningarResultatLista.length; i++) {
            tarningarResultatLista[i] = 0;
        }
        //räkna antal resultat
        for (int i = 0; i < tarningLista.length; i++) {
            //spara värdet på tarningen (från plats i) som en int
            int sidaTarning = tarningLista[i];
            //öka index på rätt plats för att öka antalet av den aktuella sidan
            //t ex om index 2 ökar till 3 så finns det tre tvåor
            tarningarResultatLista[sidaTarning]++;
        }
        
        //Om antal kast kvar är noll så stäng av knappen
        if(antalKast == 0){
            btnKasta.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnKastaActionPerformed

