    /**
     * räknar alla tärningnar med efterfrågad valör
     * @param siffraTarning efterfrågad valör på tärningen
     */
    public int siffraTarning(int siffraTarning, int[] tarningLista){
        //skapa en summa
        int summa = 0;
        
        //leta efter efterfrågad siffra och öka summan med valör "tärning" varje gång den finns
        for (int i = 0; i < tarningLista.length; i++) {
            if(tarningLista[i] == siffraTarning){
                summa += siffraTarning;
            }
        }
        
        //Uppdatera totalsumman
        totalSumma +=summa;
        
        //skriv ut totalsumman
        txfSumma.setText(String.valueOf(totalSumma));
        
        //skriv in poäng för första delen
        txfPoang.setText(String.valueOf(poang+=summa));
        
        //börja ny runda
        nyRunda();
        
        //öka bonus
        bonus += summa;
        //kolla om bonus är över 62, i så fall ge 50 extra poäng
        if (bonus >62 ){
            txfSumma.setText(String.valueOf(totalSumma += 50));
        }
        
        //Skicka tillbaka poäng för aktuell rad
        return summa;
    }
    /**
     * 
     * @param kombo den typ av kombination som letas efter (par, triss, fyrtal)
     * @param tarningarResultatLista listan med resultat
     * @return poängsumman på par/triss/fyrtal
     */
    public int letaKombo(int kombo, int[] tarningarResultatLista){
        //skapa en int med poäng
        int summaKombo = 0;
        
        //en loop som går igenom listan med antal sparade värden
        for (int i = 1; i < tarningarResultatLista.length; i++) {
            /*om något av de sparade värdena är över eller lika med kombo
            *(kombo = par/triss/fyrtal = 2, 3, 4)
            *så öka poängsumman för par med sida * kombo
            */
            if (tarningarResultatLista[i] >= kombo){
                summaKombo = i * kombo;
                
            }
        }
        return summaKombo;
    }
    public int sekventiellSokning(int[] tarningLista, int sokVarde){
        int index = -1;
        
        for (int i = 0; i < tarningLista.length; i++) {
            if (tarningLista[i] == sokVarde){
                index = i;
                break;
            }
        }
        return index;
    }
        
